num = int(input('>>> DIGITE O NÚMERO DO DIA DA SEMANA (1 A 7): '))
if num == 1:
    print('DOMINGO!')
elif num == 2:
    print('SEGUNDA-FEIRA!')
elif num == 3:
    print('TERÇA-FEIRA!')
elif num == 4:
    print('QUARTA-FEIRA!')
elif num == 5:
    print('QUINTA-FEIRA!')
elif num == 6:
    print('SEXTA-FEIRA')
elif num == 7:
    print('SÁBADO!')
else:
    print('INVÁLIDO!')
